package introducao.Operadores;

//////Aritiméticos

public class OperadoresPt1 {
    public static void main(String[] args) {
        int number1 = 10;
        int number2 = 20;

        int soma = number1 + number2;
        System.out.println(soma);

        int subtracao = number2 - number1;
        System.out.println(subtracao);

        int multiplicacao = number1 * number2;
        System.out.println(multiplicacao);

        int divisao = number2 / number1;
        ///Dois valores inteiros semrpe resultará em um valor inteiro;
        System.out.println(divisao);
    }
}
